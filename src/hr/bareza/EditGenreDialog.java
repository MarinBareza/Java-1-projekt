    private void btnEditGenreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditGenreActionPerformed
        if (formValid()) {
            try {
                
                if (parent.editGenre(new Genre(tfName.getText().trim()))) {
                    dispose();
                    MessageUtils.showInformationMessage("Information", "Genre edited successfully!");
                } else {
                    MessageUtils.showErrorMessage("Error", "Genre already exists!");
                    clearForm();
                }
            } catch (Exception e) {
                Logger.getLogger(getClass().getName()).log(Level.SEVERE, null, e);
                MessageUtils.showErrorMessage("Error", "Unable to edit Genre!");
            }
        }
    }//GEN-LAST:event_btnEditGenreActionPerformed

