    private boolean formValid() {
        lblNameError.setIcon(tfName.getText().trim().isEmpty() 
                ? new ImageIcon(getClass().getResource("/resources/icons8-monster-face-32.png")) : null);
        return !tfName.getText().trim().isEmpty();
    }

    private void clearForm() {
        tfName.setText("");
        lblNameError.setIcon(null);
    }

    private void init() {
        tfName.setText(parent.getSelectedGenre().getName());
    }
    
