    private boolean formValid() {
        lblNameError.setText(tfName.getText().trim().isEmpty() ? "*" : "");
        return !tfName.getText().trim().isEmpty();
    }

    private void clearForm() {
        tfName.setText("");
        lblNameError.setText("");
    }
    
